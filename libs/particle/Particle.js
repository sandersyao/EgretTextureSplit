var particle;
(function (particle) {
    var Particle = (function () {
        function Particle() {
            this.reset();
        }
        Particle.prototype.reset = function () {
            this.x = 0;
            this.y = 0;
            this.scale = 1;
            this.rotation = 0;
            this.alpha = 1;
            this.currentTime = 0;
            this.totalTime = 1000;
        };
        return Particle;
    })();
    particle.Particle = Particle;
    Particle.prototype.__class__ = "particle.Particle";
})(particle || (particle = {}));
